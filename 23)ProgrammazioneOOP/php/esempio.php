    //Programmazione ad oggetti: 

    /*
        La programmazione ad oggetti (OOP, Object-Oriented Programming) è un paradigma di programmazione che 
        si basa sulla creazione di oggetti, che rappresentano entità del mondo reale o concettuali, e sulla 
        loro interazione attraverso metodi e proprietà.

    L'approccio OOP si basa su quattro concetti fondamentali:

    Incapsulamento: 
    gli oggetti mantengono i loro dati e il loro comportamento interni nascosti al di fuori 
    dell'oggetto stesso, e l'accesso a essi avviene attraverso metodi pubblici e protetti.

    Ereditarietà: 
    gli oggetti possono ereditare proprietà e metodi da altri oggetti, consentendo di creare gerarchie 
    di oggetti correlati.

    Polimorfismo: 
    gli oggetti di una classe possono essere utilizzati come oggetti di una classe genitore, 
    consentendo di scrivere codice che funziona con diversi tipi di oggetti senza dover conoscere 
    il loro tipo specifico.

    Astrazione: 
    gli oggetti rappresentano entità del mondo reale o concettuali, e la loro implementazione deve essere 
    astratta e semplificata rispetto alla realtà.

    
    La programmazione ad oggetti consente di creare codice modulare e riutilizzabile, in quanto gli oggetti 
    possono essere utilizzati in diversi contesti e in diverse parti dell'applicazione. Inoltre, consente di 
    creare codice più facile da mantenere, in quanto le modifiche a un oggetto non influiscono sugli altri 
    oggetti che lo utilizzano.

    PHP è un linguaggio di programmazione che supporta la programmazione ad oggetti, 
    consentendo di creare classi e oggetti e di utilizzare i quattro concetti fondamentali dell'OOP.
    */


    
